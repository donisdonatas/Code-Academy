//1. Use the HTML DOM to set the text color of <p> to "red".
function redColor() {
    document.getElementById("demo").style.color = "red";
}

//2. Use the HTML DOM to add a yellow background color to <p>.
function yellowbackground() {
    document.getElementById("demo").style.background = "yellow";
}

//3. Use the HTML DOM to center-align the text of <p>.
function centerAlign() {
    document.getElementById("demo").style.textAlign = "center";
}

//4. Use the HTML DOM to change the text size of <p> to 40 pixels.
function changeSize() {
    document.getElementById("demo").style.fontSize = "40px";
}

//5. Use the HTML DOM to hide the <p> element.
function hideElement() {
    document.getElementById("demo").style.display = "none";
}